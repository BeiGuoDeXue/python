#!/usr/bin/python3

# 隐式转换
num_int = 123
num_float = 1.23

num_new = num_int + num_float

print("datatype of num_int:", type(num_int))
print("datatype of num_float:", type(num_float))

print("Value of num_new:",num_new)
print("datatype of num_new:",type(num_new))

# 强制转换
x = int(1.23)
print("x:", x)

x = float(1)
print("x:", x)

x = str(1)
print("x:", x)

